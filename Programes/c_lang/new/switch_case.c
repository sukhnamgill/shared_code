#include <stdio.h>
void main(){
    printf("iam running brother\n");
    //today we discuss about switch case
    char x='5';
    // printf("%c",x);
    switch(x){
        case '+':
        printf("This is Plus Button\n");
        break;

        case '-':
        printf("This is minus Button");
        break;

        case '*':
        printf("This is multiply Button");
        break;

        default:
        printf("This is default value of control statement");
    }




}